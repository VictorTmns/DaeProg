void DrawRectangles() {
	for (int i = 0; i < 100; i++)
	{
		const int width{ rand() % 100 };
		const int height{ rand() % 100 };
		const int leftX{ 10 + (rand() % int(g_WindowWidth - width - 20)) };
		const int bottomY{ 10 + (rand() % int(g_WindowHeight - height - 20)) };
		const Color4f randomColor{ float((rand() % 1000) / 1000.f), float((rand() % 1000) / 1000.f),  float((rand() % 1000) / 1000.f),  1 };

		SetColor(randomColor);
		FillRect(float(leftX), float(bottomY), float(width), float(height));
		SetColor(0.f, 0.f, 0.f, 1.f);
		DrawRect(float(leftX), float(bottomY), float(width), float(height));
	}
}
