void DrawRectangles() {
	const int randomNumber{ rand() };
	const int leftX{ 10 + (randomNumber % int(g_WindowHeight) - 20)};
	const int bottomY{ 10 + (randomNumber % int(g_WindowWidth) - 20)};
	const int width{ randomNumber % 100};
	const int height{ randomNumber % 100};
	const Color4f randomColor{ float((randomNumber % 1000) / 1000.f), float((randomNumber % 1000) / 1000.f),  float((randomNumber % 1000) / 1000.f),  1};

	SetColor(randomColor);
	FillRect(float(leftX), float(bottomY), float(width), float(height));
}
